public class Delivery {
    private int statYear;
    private int statMonth;
    private int deliveryQuota;
    private int deliveryQty;

    public int getStatYear() {
        return statYear;
    }

    public void setStatYear(int statYear) {
        this.statYear = statYear;
    }

    public int getStatMonth() {
        return statMonth;
    }

    public void setStatMonth(int statMonth) {
        this.statMonth = statMonth;
    }

    public int getDeliveryQuota() {
        return deliveryQuota;
    }

    public void setDeliveryQuota(int deliveryQuota) {
        this.deliveryQuota = deliveryQuota;
    }

    public int getDeliveryQty() {
        return deliveryQty;
    }

    public void setDeliveryQty(int deliveryQty) {
        this.deliveryQty = deliveryQty;
    }
}
